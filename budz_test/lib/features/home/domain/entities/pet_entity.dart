class PetEntity {
  final String id;
  final String name;
  final String gender;
  final String age;
  final double weight;
  final String breed;
  final String imageUrl;

  PetEntity({
    required this.id,
    required this.name,
    required this.gender,
    required this.age,
    required this.weight,
    required this.breed,
    required this.imageUrl,
  });
}
